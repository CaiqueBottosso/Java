public class ExampleVariable {
    public static void main(String[] args) {
        String firstName = "Frodo"; //string data type = contain text
        String lastName= "Baggins";
        int age = 120; // whole number
        char gender = 'M'; //singular character
        Boolean isAdventurer = true; //Boolean contains True and False
        float  weight = 120.5f; //contain decimal number

        System.out.println("Adventurer first name = " +firstName);
        System.out.println(lastName);
        System.out.println(age);
        System.out.println("Adventurer gender = "+ gender);
        System.out.println("Adventurer is brave? "+ isAdventurer);
        System.out.println("His wight is: " +weight+ "kg");

    }
}
